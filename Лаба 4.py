def get_positive_integer():
    """Запрашивает у пользователя положительное целое число и повторяет ввод при ошибке."""
    while True:
        try:
            number = int(input("Введите длину отрезка (целое положительное число): "))
            if number > 0:
                return number
            else:
                print("Ошибка: число должно быть положительным!")
        except ValueError:
            print("Ошибка: введите целое число!")

def check_right_triangle(a, b, c):
    """Проверяет, могут ли отрезки образовать прямоугольный треугольник."""
    # Сортируем стороны, чтобы наибольшая была последней (гипотенузой)
    sides = sorted([a, b, c])
    
    # Проверяем теорему Пифагора: a² + b² = c²
    return sides[0]**2 + sides[1]**2 == sides[2]**2

def main():
    print("Проверка возможности построения прямоугольного треугольника")
    print("=" * 50)
    
    # Получаем три отрезка от пользователя
    print("Введите три отрезка:")
    side1 = get_positive_integer()
    side2 = get_positive_integer()
    side3 = get_positive_integer()
    
    print(f"\nВведены отрезки: {side1}, {side2}, {side3}")
    
    # Проверяем условие прямоугольного треугольника
    if check_right_triangle(side1, side2, side3):
        print("✓ Результат: Отрезки МОГУТ образовать прямоугольный треугольник")
    else:
        print("✗ Результат: Отрезки НЕ МОГУТ образовать прямоугольный треугольник")

# Запуск программы
if __name__ == "__main__":
    main()